package org.graphics;

public class Squares {

	private float x;
	private float xl;
	private float y;
	private float yl;
	
	public Squares(float x, float xl, float y, float yl){
		
		this.x = x;
		this.xl = xl;
		this.y = y;
		this.yl = yl;
		
	}
	
	public float getX(){
		
		return this.x;
		
	}
	
	public float getY(){
		
		return this.y;
		
	}
	
	public float getXL(){
		
		return this.xl;
		
	}
	
	public float getYL(){
		
		return this.yl;
		
	}
	
	public void setX(float x){
		
		this.x = x;
		
	}
	
	public void setY(float x){
		
		this.y = x;
		
	}
	
	public void setXL(float x){
		
		this.xl = x;
		
	}
	
	public void setYL(float x){
		
		this.yl = x;
		
	}

	
}
